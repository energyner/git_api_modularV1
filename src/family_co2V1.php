    <p>This carbon calculator is free to use</p>
    <p>The carbon footprint is the total amount of CO2 that are generated daily by our actions in our homes, businesses, neighborhood or workgroups. It is equivalent to the amount of polluting gases that are released in combustion processes (these gases are known as Greenhouse Gases), where the highest percentage is CO2 gas. The combustion is a chemical process that is present in almost all  energy systems that we use every day. Examples of systems are the transportations (air, land, sea) where diferents kinds of fuel oil are burnerd; the use of electricity from Thermoelectric Plants that burn fossil fuels (Natural Gas, Diesel, Fuel Oil ). We consume electricity for lighting, air conditioning, refrigerating and preserving foods, in the electric motors and others services.  The use of water which requires compression systems;  and in each food, good, service that we can use every day. <br>
    This calculator compares the emissions that we generate monthly with figures of the average US's States  and the world, in order to show how much we are influencing on air pollution with our actions vs the reference numbers. <br>
