function potencia() {
    // Obtener valores desde el formulario con conversión segura
    let state = document.cálculo.state.value;
    if (state === "Select your State") {
        document.cálculo.resultado.value = "Waiting for State";
        return false;
    }

    let elect = parseFloat(document.cálculo.elect.value) || 0;
    let gas = parseFloat(document.cálculo.gas.value) || 0;
    let water = parseFloat(document.cálculo.water.value) || 0;
    let lpg = parseFloat(document.cálculo.lpg.value) || 0;
    let gn = parseFloat(document.cálculo.gn.value) || 0;
    let fly = parseFloat(document.cálculo.fly.value) || 0;
    let cogs = parseFloat(document.cálculo.cogs.value) || 0;
    let person = parseFloat(document.cálculo.person.value) || 1;

    // Definir coeficiente de vuelo
    let coef_fly = fly < 1000 ? 0.55 : 0.33;

    // Calcular emisiones de CO2
    elect = Math.round(elect * 0.991);
    gas = Math.round(gas * 19.6);
    water = Math.round(water * 0.54);
    lpg = Math.round(lpg * 3);
    gn = Math.round(gn * 0.117);
    fly = Math.round(fly * coef_fly);
    cogs = Math.round(cogs * 0.53);

    // Actualizar los valores en el formulario
    document.cálculo.reporte_e.value = elect;
    document.cálculo.reporte_gas.value = gas;
    document.cálculo.reporte_water.value = water;
    document.cálculo.reporte_lpg.value = lpg;
    document.cálculo.reporte_gn.value = gn;
    document.cálculo.reporte_fly.value = fly;
    document.cálculo.reporte_cogs.value = cogs;

    // Cálculo total y per cápita
    let resultado = elect + gas + water + lpg + gn + fly + cogs;
    let percapita = Math.round(resultado / person);
    
    document.cálculo.resultado.value = resultado;
    document.cálculo.percapita.value = percapita;

    // Asignación de datos por estado
    let state_data = {
        "AL": [50942.9, 4245.2], "AK": [108449.6, 9037.5], "AZ": [27577.3, 2298.1], "AR": [52502.8, 4375.2],
        "CA": [20938.8, 1744.9], "CO": [34536.4, 2878], "CT": [23566.2, 1963.8], "DE": [30899.1, 2574.9], 
        "FL": [23841.7, 1986.8], "GA": [27964.4, 2330.4]
        // Agregar más estados según sea necesario...
    };

    let [total_state, per_cap_state] = state_data[state] || [0, 0];
    document.cálculo.state_us.value = state;
    document.cálculo.per_cap_m.value = per_cap_state;

    // Calcular porcentaje
    let porcent = Math.round((percapita / per_cap_state) * 100);
    document.cálculo.porcent.value = porcent;

    // Calcular porcentaje individual por consumo
    let percentages = {
        "pcent_elect": Math.round((elect / resultado) * 100),
        "pcent_gas": Math.round((gas / resultado) * 100),
        "pcent_water": Math.round((water / resultado) * 100),
        "pcent_lpg": Math.round((lpg / resultado) * 100),
        "pcent_gn": Math.round((gn / resultado) * 100),
        "pcent_fly": Math.round((fly / resultado) * 100),
        "pcent_cogs": Math.round((cogs / resultado) * 100)
    };

    // Guardar en localStorage
    Object.entries(percentages).forEach(([key, value]) => localStorage.setItem(key, value));
}
